package enums;

/**
 *
 * @author Moi
 */
public enum TypeMoteur {
  Deux("Deux temps", 1),
  Troix("Trois Temps", 2);

  public String name;
  public int code;
  
  //Constructeur
  TypeMoteur(String name,int code){
    this.code = code;
    this.name = name;
  }

    public String getName() {
        return name;
    }

    public int getCode() {
        return code;
    }
}
